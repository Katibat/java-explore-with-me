CREATE TABLE IF NOT EXISTS USERS
(
    ID    BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NAME  VARCHAR(256)                            NOT NULL,
    EMAIL VARCHAR(512)                            NOT NULL,
    CONSTRAINT PK_USER PRIMARY KEY (ID),
    CONSTRAINT UQ_USER_EMAIL UNIQUE (EMAIL)
);

CREATE TABLE IF NOT EXISTS CATEGORIES
(
    ID   BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NAME VARCHAR(256)                            NOT NULL,
    CONSTRAINT PK_CATEGORIES PRIMARY KEY (ID)
);

CREATE TABLE IF NOT EXISTS EVENTS
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY  NOT NULL,
    TITLE VARCHAR(128)                          NOT NULL,
    ANNOTATION VARCHAR(512)                     NOT NULL,
    DESCRIPTION VARCHAR(1024)                   NOT NULL,
    CATEGORY_ID BIGINT,
    CREATED TIMESTAMP,
    EVENT_DATE TIMESTAMP,
    INITIATOR_ID BIGINT                         NOT NULL,
    PAID BOOLEAN,
    PARTICIPANT_LIMIT BIGINT DEFAULT 0          NOT NULL,
    PUBLISHED_ON TIMESTAMP,
    REQUEST_MODERATION BOOLEAN,
    STATE VARCHAR(20)                           NOT NULL,
    CONSTRAINT PK_EVENT PRIMARY KEY (ID),
    CONSTRAINT FK_EVENT_ON_CATEGORY FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORIES (ID),
    CONSTRAINT FK_EVENT_ON_USER FOREIGN KEY (INITIATOR_ID) REFERENCES USERS (ID)
 );

CREATE TABLE IF NOT EXISTS EVENT_LOCATIONS
(
    EVENT_ID BIGINT                             NOT NULL
        CONSTRAINT EVENT_ID_LOCATION REFERENCES EVENTS
            ON UPDATE CASCADE ON DELETE CASCADE,
    LAT      NUMERIC                            NOT NULL,
    LON      NUMERIC                            NOT NULL,
    CONSTRAINT FK_LOCATION FOREIGN KEY (EVENT_ID) REFERENCES EVENTS (ID)
);

CREATE TABLE IF NOT EXISTS REQUESTS
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    EVENT_ID     BIGINT                        NOT NULL,
    REQUESTER_ID BIGINT                        NOT NULL,
    STATUS       VARCHAR,
    CREATED      TIMESTAMP,
    CONSTRAINT PK_REQUEST PRIMARY KEY (ID),
    UNIQUE (REQUESTER_ID, EVENT_ID),
    CONSTRAINT FK_REQUESTER FOREIGN KEY (REQUESTER_ID) REFERENCES USERS (ID),
    CONSTRAINT FK_EVENTS_REQUESTS FOREIGN KEY (EVENT_ID) REFERENCES EVENTS (ID)
);

CREATE TABLE IF NOT EXISTS COMPILATIONS
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    TITLE  VARCHAR(256)                        NOT NULL,
    PINNED BOOLEAN,
    CONSTRAINT PK_COMPILATIONS PRIMARY KEY (ID)
);

CREATE TABLE IF NOT EXISTS EVENTS_COMPILATION
(
    ID     BIGINT GENERATED BY DEFAULT AS IDENTITY      NOT NULL,
    EVENT_ID       BIGINT                               NOT NULL,
    COMPILATION_ID BIGINT                               NOT NULL,
    CONSTRAINT FK_EVENTS_COMPILATION_ON_COMPILATION FOREIGN KEY (COMPILATION_ID) REFERENCES COMPILATIONS (ID),
    CONSTRAINT FK_EVENTS_COMPILATION FOREIGN KEY (EVENT_ID) REFERENCES EVENTS (ID)
);